% A simple example of linear regression
% Xin Tang @ IMF 2019
% From Using Modern Programming Tools in Macroeconomics

X = [ 1.0 7.0  5.0 6.0; ...
      1.0 2.0 -1.0 6.0; ...
      1.0 7.0  3.0 5.0; ...
      1.0 -3.0  1.0 4.0; ...
      1.0 2.0 -1.0 0.0; ...
      1.0 2.0  1.0 7.0; ...
      1.0 -3.0 -1.0 3.0; ...
      1.0 2.0  1.0 1.0; ...
      1.0 2.0  1.0 4.0];

Y = [7.0 -5.0 6.0 5.0 5.0 -2.0 0.0 8.0 3.0];
Y = Y';

beta = inv(X'*X)*X'*Y;

disp(beta)